// Используется формула расстояния между двумя точками на плоскости: sqrt((x2 - x1)^2 + (y2 - y1)^2).
// Функция Math.sqrt используется для вычисления квадратного корня, а Math.pow - для возведения в степень.
// Расстояние вычисляется и возвращается как результат.
// Метод main:
